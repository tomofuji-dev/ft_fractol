#include "libft.h"

char	*ft_strnstr(const char *big, const char *little, size_t len)
{
	size_t	i;

	if (*little == '\0')
		return ((char *)big);
	while (len-- > 0)
	{
		i = 0;
		while (big[i] == little[i])
		{
			if (little[i + 1] == '\0')
				return ((char *)big);
			if (i >= len)
				return (NULL);
			i++;
		}
		if (big[i] == '\0')
			return (NULL);
		big++;
	}
	return (NULL);
}
